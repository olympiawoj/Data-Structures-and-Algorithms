


//Singly Linked List Node
class Node {
  constructor(val) {
    this.val = val;
    // at beginning, nothing comes after
    this.next = null;
  }
}


//Double Linked List(LL) Node
class DoublyLLNode {
  constructor(val) {
    this.val = val;
    // at beginning, nothing comes after
    this.next = null;
    this.previous = null
  }
}
